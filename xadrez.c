#include <stdio.h>

int main() {
    // mover a torre 7 casas para cima
    for (int i = 0; i < 7; i++) {
        printf ("cima\n"); 
    }
    // mover bisbo 2 casas para direita
    for (int i = 0; i < 2; i++) {
        printf ("direita\n"); 
    }
    // mover rainha 1 casa para direita
    for (int i = 0; i < 1; i++) {
        printf ("direita\n"); 
    }
    return 0;
}